int iz = 2, iy = 3;
float fr;

/* 錯誤方式：先整數除，結果是 0.0，再存到 fr */
fr = iz / iy;           /* 0.000000 */

/* 正確方式一：強制轉型 iz 為 float，觸發浮點除法 */
fr = (float)iz / iy;    /* 2.0f / 3 → 2.0f / 3.0f = 0.666667 */

/* 正確方式二：強制轉型整個結果（錯！先整數除再轉型） */
fr = (float)(iz / iy);  /* (2/3) = 0，再轉 0.0f */

printf("正確：%.6f\n", (float)iz / iy);   /* 0.666667 */
printf("錯誤：%.6f\n", (float)(iz / iy)); /* 0.000000 */